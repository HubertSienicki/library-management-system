    private void loginToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginToggleButtonActionPerformed
        if(this.getName().isEmpty() || this.getPassword().isEmpty()){
            JOptionPane.showMessageDialog(null, "Either of the fields is empty, please try again", "Warning", JOptionPane.ERROR_MESSAGE);
            this.invalidate();
            this.validate();
            this.repaint();
        }else{
            checkValidCredentials check = new checkValidCredentials(this.getUsername(), this.getPassword());
            if(check.checkValidCredentials()){
                MainPage m = new MainPage();
                this.dispose();
                m.setVisible(true);
            }
        }
    }//GEN-LAST:event_loginToggleButtonActionPerformed

